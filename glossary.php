    <article>
      <div class="container">
        <div class="row">
          <div class="col-md-12">
            <h2 class="tc">Glossary</h2>
            <hr>
            <div class="well well-md">
              <div class="col-md-3">
                <div class="term">
                  <h2>A</h2>
                  <hr>
                  <strong>Above the fold</strong>
                  <p>
                    Ad positioning in the top part of a page so that it will
                    be visible without scrolling down.
                  </p>
                  <strong>Action</strong>
                  <p>
                    Advertisement interaction criteria; could be downloading
                    software, signing up to a service, purchasing an item, etc.
                    Often addressed as “Conversion”, i.e. completing the action
                    criteria.
                  </p>
                  <strong>Ad Exchange</strong>
                  <p>
                    An ad exchange is a sales channel between supply and demand
                    offering real-time capabilities. Ad exchanges' business models
                    and practices may include features that are similar to
                    those offered by ad networks.
                  </p>
                  <strong>Ad Format</strong>
                  <p>
                    Size and type of ad: may include sizing (250X300, 120X600),
                    format; text, video, rich media, type; banner, pop up, pop under,
                    catfish, etc.
                  </p>
                  <strong>Ad server</strong>
                  <p>
                    An ad server is a web server dedicated to the delivery of
                    advertisement. This specialization enables the tracking and
                    management of advertising related metrics.
                  </p>
                  <strong>Ad Tag</strong>
                  <p>
                    Software code that is provided to a publisher or ad network
                    that calls the advertisers ad server for the purposes of
                    displaying an advertisement in accordance with tag criteria.
                  </p>
                  <strong>Advertiser</strong>
                  <p>
                    The party who ordered (and is generally paying) for an advertisement.
                  </p>
                  <strong>Advertisement</strong>
                  <p>
                    A commercial message targeted to an advertiser’s customer or prospect.
                  </p>
                </div>
              </div>
              <div class="col-md-3">
                <div class="term">
                  <h2>B</h2>
                  <hr>
                  <strong>Backup ad</strong>
                  <p>
                    An ad that will be automatically inserted if bidding will
                    not reach minimal target.
                  </p>
                  <strong>Below the fold</strong>
                  <p>
                    Ad positioning that will require user to scroll down in
                    order to view the ad.
                  </p>
                </div>
                <div class="term">
                  <h2>C</h2>
                  <hr>
                  <strong>Click</strong>
                  <p>
                    When a user clicks on an ad.
                  </p>
                  <strong>Click through rate (CTR)</strong>
                  <p>
                    The ratio of ads that where clicked out of a set of
                    impressions. Note this ratio is sensitive to the number of
                    ads you are showing on a given page.
                  </p>
                  <strong>CPA</strong>
                  <p>
                    Cost per Action the advertiser has committed to pay
                  </p>
                  <strong>CPC</strong>
                  <p>
                    Cost per Click the advertiser has committed to pay
                  </p>
                  <strong>CPL</strong>
                  <p>
                    Cost per Lead the advertiser has committed to pay
                  </p>
                  <strong>CPM</strong>
                  <p>
                    Cost per Mille (thousand), cost per thousand ad views the
                    advertiser has committed to pay. Often eCPM (effective) is
                    used as a bridging model in order to compare different pricing formats.

                    There are additional pricing formats we will not dive into such as:
                      CPE: Cost per engagement.
                      CPV: Cost per view (usually videos).
                      CPS: Cost per sale, usually a share of the sale.
                      CPI: Cost per installation, unique type of CPA.

                  </p>
                  <strong>Coverage</strong>
                  <p>
                    Ratio of impressions out of the total ad requests.
                  </p>
                </div>
              </div>
              <div class="col-md-3">
                <div class="term">
                  <h2>D</h2>
                  <hr>
                  <strong>Data</strong>
                  <p>
                    Attributes associated with an ad request that offer
                    information on the user viewing the ad, on the system that
                    user uses to view the ad, the website where the ad is presented
                    and more. These attributes ad value to an impression from an
                    advertiser standpoint and is most important for real time/automated
                    bidding. Naming some attributes: Platform, OS, screen resolution,
                    user common languages, age, gender, geo location, user interest
                    categories, website interest categories, content rating, brand
                    safety and more.
                  </p>
                  <strong>Demand</strong>
                  <p>
                    Parties interested in purchasing ad spaces in order to
                    serve advertisement.
                  </p>
                  <strong>Dynamic ads</strong>
                  <p>
                    Ad formats that can dynamically change in size and format
                    in order to best accommodate the serving platform (desktop VS mobile for example).
                  </p>
                </div>
                <div class="term">
                  <h2>P</h2>
                  <hr>
                  <strong>Pop Up Ad</strong>
                  <p>
                    An ad that is opened in a new browser window (entire window)
                    that will spring above the currently viewed browser window
                    (disruptive browsing activity).
                  </p>
                  <strong>Pop Under AD</strong>
                  <p>
                    An ad that is opened in a new browser window (entire window)
                    that will spring behind the currently viewed browser window
                    (non-disruptive of browsing activity).
                  </p>
                  <strong>Publisher</strong>
                  <p>
                    Owner of a website or other online channel that can present ads.
                  </p>
                </div>
              </div>
              <div class="col-md-3">
                <div class="term">
                  <h2>R</h2>
                  <hr>
                  <strong>RPM</strong>
                  <p>
                    Revenue per mille (thousand), averaged revenue gained by
                    the publisher for each thousand impressions. RPM normalizes
                    revenue gained from CPA, CPC, CPM (…) ads.
                  </p>
                  <strong>Rich media</strong>
                  <p>
                    Non-standard advertisements with which users can interact with.
                    These advertisements can be used either singularly or in
                    combination with various technologies (sound, video, Flash,
                    Javascript and more). Ads may appear in ad formats such as
                    banners and buttons as well as transitional ads and various
                    over-the-page units such as floating ads, page take-overs,
                    catfish and tear-backs.
                  </p>
                </div>
                <div class="term">
                  <h2>S</h2>
                  <hr>
                  <strong>Supply</strong>
                  <p>
                    Parties interested in selling ad spaces for monetization
                    purpose. With this respect, Adbarn is a SSP (sell side platform)
                    that aggregates potential ad slots form multiple publishers.
                  </p>
                </div>
              </div>
              <div class="clearfix"></div>
            </div>
          </div>
        </div>
      </div>
    </article>

